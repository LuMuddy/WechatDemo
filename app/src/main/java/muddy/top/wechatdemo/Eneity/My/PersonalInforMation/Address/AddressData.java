package muddy.top.wechatdemo.Eneity.My.PersonalInforMation.Address;

public class AddressData {
    private String addressName;
    private String Address;
    private String mobile;
    private String DetailedArea;
    private String dak;

    public String getAddressName() {
        return addressName;
    }

    public void setAddressName(String addressName) {
        this.addressName = addressName;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getDetailedArea() {
        return DetailedArea;
    }

    public void setDetailedArea(String detailedArea) {
        DetailedArea = detailedArea;
    }

    public String getDak() {
        return dak;
    }

    public void setDak(String dak) {
        this.dak = dak;
    }

}
